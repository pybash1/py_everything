# Mass
class mg:
    '''Milligram class'''
    def __init__(self):
        self.name: str = 'MilliGram'
        self.unit: str = 'mg'
    
    def __repr__(self) -> str:
        return f'mg(name={self.name}, unit={self.unit})'

    def __str__(self) -> str:
        return f'{self.unit}' 

